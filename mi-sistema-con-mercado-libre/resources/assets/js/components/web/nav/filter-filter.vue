<template>
		<li >
			<a v-tooltip.top-left="msg" @click.prevent="deleteFilter" href="#">{{filter.value_name}}</a>
		</li>
</template>

<script>
import {mapGetters} from 'vuex';
import collect from 'collect.js';
import {Event} from 'vue-tables-2';
export default {
		props : ['filter'],
		data(){
				return {
						msg : 'Remover filtro'
				}
		},
		
		methods : {
				
				deleteFilter(){

					this.$store.commit('DELETE_FILTER', this.filter);
					
					this.$store.commit('FORCE_INIT_PAGINATION');

                    this.$store.dispatch('filtered_data')
                    .then((result) => {

						this.$store.commit('CLEAR_PUBLICATIONS');
						
						this.$store.commit('SET_PUBLICATIONS_WEB_WITH_PAGINATION', result.data.data);

						this.$store.commit('SET_PAGINATION', result.data.pagination)
						
                    }).catch((err) => {
                        
                    });

				}
		},

	 
		
		computed : {
			 
		},

		mounted() {
				
		},
}
</script>
